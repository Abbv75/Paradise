    private void btn_connexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_connexionActionPerformed
        // TODO add your handling code here:
        String login = txt_mail.getText();
        String mdp = txt_pass.getText();
        String Role = txt_role.getSelectedItem().toString();
         String urlBDD = "jdbc:mysql://localhost:3306/paradis2.0";
        String user = "root";
        String passwd = "";
        
                try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection(urlBDD, user, passwd);
            
                     String requete = "SELECT * FROM user WHERE email=? AND mot_de_passe=? AND role=?";
            PreparedStatement state = conn.prepareStatement(requete);
            state.setString(1, login);
            state.setString(2, mdp);
            state.setString(3, Role);
            ResultSet result = state.executeQuery();

            if (login.isEmpty() || mdp.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Les champs ne doit Ãªtre vide.", "Champs", JOptionPane.INFORMATION_MESSAGE);
            } else {
                if (!result.wasNull() && result.first()) {
                    if (Role=="admin"){
                         new Interface_admin().setVisible(true);
                         this.dispose();
                    } else if (Role == "medecin"){
                        new Interface_docteur ().setVisible(true);
                        this.dispose();
                    } else if (Role == "infirmier"){
                        new Interface_infirmier ().setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Role incorrect", "erreur de login et/ou mot de passe", JOptionPane.ERROR_MESSAGE);
                    }
                    
                   
                } 
                else {
                    JOptionPane.showMessageDialog(this, "ERREUR", "erreur de login et/ou mot de passe", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Erreur de connexion");
        }
    }//GEN-LAST:event_btn_connexionActionPerformed

