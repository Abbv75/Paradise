    public void remplir_bloc(){
        if(C1.try_connection()){
            ResultSet response=C1.simple_select("bloc");
           
            ArrayList<String> block_request=new ArrayList<String>();
            try{
                int i=0;
                while(response.next()){
                    
                    block_request.add(response.getString("nom"));
                }
                i=block_request.size();
                String[] block_list=new String[i];
                for(i=0;i<block_request.size();i++){
                    block_list[i]=block_request.get(i);
                }
                enregistrement_block.setModel(new javax.swing.DefaultComboBoxModel<>(block_list));
            }
            catch(Exception e){
                e.printStackTrace();   
            } 
        }
    }
    
    public boolean enregister(){
        SimpleDateFormat dateFormater=new SimpleDateFormat("yyyy-MM-dd");
        head[0]="nom";
        head[1]="prenom";
        head[2]="genre";
        head[3]="date_naissance";
        head[4]="tel";
        //head[5]="nom";
        
        fields[0]=enregistrement_nom.getText();
        fields[1]=enregistrement_prenom.getText();
        fields[2]=(String)enregistrement_genre.getSelectedItem();
        fields[3]=dateFormater.format(new Date(String.valueOf(enregistrement_date.getDate())));
        fields[4]=enregistrement_tel.getText();
        //fields[5]=enregistrement_motif.getText();
        
        if(C1.insert_into("patient", head, fields)){
            if(createTicket()){
                try{
                    ResultSet select =C1.simple_select("ticket");
                    select.next();
                    ResultSet bloc =C1.select_from_id("bloc",Integer.parseInt(select.getString("id_bloc")));
                    bloc.next();
                    JOptionPane.showMessageDialog(this, "Votre numero de ticket est"+select.getString("id")+select.getString("numero")+"\nDirigez vous vers le bloc "+bloc.getString("nom"), "Ticket No"+select.getString("id")+select.getString("numero"), JOptionPane.INFORMATION_MESSAGE);

                    return true;
                }
                catch(Exception e){
                    e.printStackTrace();
                    return false;
                }
            }
            else{
                return false;
            }
        }
        else{
            return false;
        }
    }  
    
    public boolean createTicket(){
        try{
            ResultSet query=catchPatient(fields[0],fields[1],fields[4]);
            if(query!=null){
                query.next();
                
                String[] ticketHead=new String[4];
                ticketHead[0]="numero";
                ticketHead[1]="motif";
                ticketHead[2]="id_bloc";
                ticketHead[3]="id_patient";
                
                String[] ticketContent=new String[4];
                ticketNumber++;
                ticketContent[0]="A"+ticketNumber;
                ticketContent[1]=enregistrement_motif.getText();
                ResultSet blockId=catchBloc((String) enregistrement_block.getSelectedItem());
                if(blockId!=null){
                    blockId.next();
                    ticketContent[2]=blockId.getString("id");
                }
                else{
                    return false;
                }
                ticketContent[3]=query.getString("id");
                if(C1.insert_into("ticket", ticketHead, ticketContent)){
                    return true;
                }
                else{
                    return false;
                }  
            }
        }
        catch(Exception e){
            e.printStackTrace();
            System.out.println("Erreur de connexion");
            return false;
        }
        return false;
    }
    
    public ResultSet catchPatient(String nom, String prenom, String tel){
        String query="SELECT * FROM patient WHERE nom=? AND prenom=? AND tel=?;";
        Object resultat=new Object();
        C1.try_connection();
        try{
            PreparedStatement stm= C1.conn.prepareStatement(query);
            stm.setString(1, nom);
            stm.setString(2, prenom);
            stm.setString(3, tel);
            resultat=(ResultSet)stm.executeQuery();
            
        }
        catch (Exception e){
            e.printStackTrace();
            System.out.println("Erreur de connexion");
            resultat=null;
        }
        return (ResultSet)resultat;
    }
    public ResultSet catchBloc(String nom){
        String query="SELECT * FROM bloc WHERE nom=?;";
        Object resultat=new Object();
        C1.try_connection();
        try{
            PreparedStatement stm= C1.conn.prepareStatement(query);
            stm.setString(1, nom);
            resultat=(ResultSet)stm.executeQuery(); 
        }
        catch (Exception e){
            e.printStackTrace();
            System.out.println("Erreur de connexion");
            resultat=null;
        }
        return (ResultSet)resultat;
    }

